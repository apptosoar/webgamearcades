function renderPlayer(gameId) {
  const game = getGames().find((item) => item.id === gameId);

  if (!game) {
    app.innerHTML = `<section class="section"><div class="empty-state">${copy.gameNotFound}</div></section>`;
    return;
  }

  app.innerHTML = `
    <section class="section player-shell">
      <div class="player-top">
        <div>
          <div class="eyebrow">${getGenre(game.genre).name}</div>
          <h2>${game.title}</h2>
        </div>
        <div class="hero-actions">
          <a class="ghost-button" href="#games/${game.genre}">${copy.list}</a>
        </div>
      </div>
      <div class="iframe-frame">
        ${gameFrame(game)}
      </div>
    </section>
  `;
}

function gameFrame(game) {
  if (game.url) {
    return `<iframe title="${game.title}" src="${escapeAttribute(game.url)}" sandbox="allow-scripts allow-same-origin"></iframe>`;
  }

  return `<iframe title="${game.title}" srcdoc="${escapeAttribute(game.srcdoc)}" sandbox="allow-scripts"></iframe>`;
}

