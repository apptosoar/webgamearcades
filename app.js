function gamesSection(genreId = "all", standalone = false) {
  const visibleGames = genreId === "all" ? getGames() : getGames().filter((game) => game.genre === genreId);
  const title = genreId === "all" ? copy.allGames : copy.genreGames.replace("{genre}", getGenre(genreId).name);
  return `
    <section class="section" id="games">
      <div class="section-header">
        <div>
          <div class="eyebrow">${copy.gamesEyebrow}</div>
          <h2>${title}</h2>
          <p>${copy.gamesText}</p>
        </div>
        ${standalone ? `<a class="ghost-button" href="#home">${copy.home}</a>` : ""}
      </div>
      <div class="toolbar">
        <div class="genre-filter" aria-label="${copy.gameGenreFilter}">
          ${["all", ...getGenres().map((genre) => genre.id)]
            .map((id) => {
              const label = id === "all" ? copy.all : getGenre(id).name;
              return `<button class="${id === genreId ? "active" : ""}" type="button" data-filter="${id}">${label}</button>`;
            })
            .join("")}
        </div>
        ${genreId === "all" ? "" : `<button class="primary-button" type="button" data-watch-ad>${copy.watchAd}</button>`}
      </div>
      <div class="grid">
        ${visibleGames.map(gameCard).join("")}
      </div>
    </section>
  `;
}

function gameCard(game) {
  return `
    <article class="game-card" style="--accent:${game.accent}">
      <div class="game-top">
        <span class="tile-icon">${getGenre(game.genre).icon}</span>
        <h3>${game.title}</h3>
        <p>${game.description}</p>
      </div>
      <div class="game-actions">
        <span class="cost"><span class="coin-icon"></span>1</span>
        <button class="primary-button" type="button" data-launch="${game.id}">${copy.launch}</button>
      </div>
    </article>
  `;
}

function handleAppClick(event) {
  const adButton = event.target.closest("[data-watch-ad]");
  if (adButton) {
    openRewardAd();
    return;
  }

  const filterButton = event.target.closest("[data-filter]");
  if (filterButton) {
    location.hash = `#games/${filterButton.dataset.filter}`;
    return;
  }

  const launchButton = event.target.closest("[data-launch]");
  if (launchButton) {
    launchGame(launchButton.dataset.launch);
  }
}

function launchGame(gameId) {
  if (state.coins < 1) {
    const game = getGames().find((item) => item.id === gameId);
    const genreId = game?.genre || "all";
    location.hash = `#games/${genreId}`;
    requestAnimationFrame(() => {
      const section = document.querySelector("#games .section-header p");
      if (section) {
        section.textContent = copy.notEnoughCoins;
      }
    });
    return;
  }

  state.coins -= 1;
  persistCoins();
  location.hash = `#play/${gameId}`;
}

function openRewardAd() {
  clearInterval(state.adTimer);
  let progress = 0;
  adModal.hidden = false;
  finishAdButton.disabled = true;
  adProgress.style.width = "0%";

  state.adTimer = setInterval(() => {
    progress += 10;
    adProgress.style.width = `${progress}%`;
    if (progress >= 100) {
      clearInterval(state.adTimer);
      finishAdButton.disabled = false;
    }
  }, 280);
}

function closeRewardAd() {
  clearInterval(state.adTimer);
  adModal.hidden = true;
}

function claimReward() {
  state.coins += 1;
  persistCoins();
  closeRewardAd();
}

function persistCoins() {
  localStorage.setItem("gamecoin.coins", String(state.coins));
  updateCoins();
}

function updateCoins() {
  coinCount.textContent = state.coins;
}

function getGenres() {
  return genreMeta.map((genre) => ({
    ...genre,
    name: copy.genres[genre.id].name,
    description: copy.genres[genre.id].description,
  }));
}

function getGenre(id) {
  return getGenres().find((genre) => genre.id === id) || getGenres()[0];
}

function getGames() {
  return gameMeta.map((game) => ({
    ...game,
    description: copy.gameDescriptions[game.id],
    srcdoc: game.makeSrcdoc ? game.makeSrcdoc() : undefined,
  }));
}

function escapeAttribute(value) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll('"', "&quot;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;");
}

function jsString(value) {
  return JSON.stringify(value);
}

