function gamesSection(genreId = "all", standalone = false) {
  const visibleGames = genreId === "all" ? getGames() : getGames().filter((game) => game.genre === genreId);
  const title = genreId === "all" ? copy.allGames : copy.genreGames.replace("{genre}", getGenre(genreId).name);
  return `
    <section class="section" id="games">
      <div class="section-header">
        <div>
          <div class="eyebrow">${copy.gamesEyebrow}</div>
          <h2>${title}</h2>
          <p>${copy.gamesText}</p>
        </div>
        ${standalone ? `<a class="ghost-button" href="#home">${copy.home}</a>` : ""}
      </div>
      <div class="toolbar">
        <div class="genre-filter" aria-label="${copy.gameGenreFilter}">
          ${["all", ...getGenres().map((genre) => genre.id)]
            .map((id) => {
              const label = id === "all" ? copy.all : getGenre(id).name;
              return `<button class="${id === genreId ? "active" : ""}" type="button" data-filter="${id}">${label}</button>`;
            })
            .join("")}
        </div>
      </div>
      <div class="grid">
        ${visibleGames.map(gameCard).join("")}
      </div>
    </section>
  `;
}

function gameCard(game) {
  return `
    <article class="game-card" style="--accent:${game.accent}">
      <div class="game-top">
        <span class="tile-icon">${game.icon || getGenre(game.genre).icon}</span>
        <h3>${game.title}</h3>
        <p>${game.description}</p>
      </div>
      <div class="game-actions">
        <button class="primary-button" type="button" data-launch="${game.id}">${copy.launch}</button>
      </div>
    </article>
  `;
}

function handleAppClick(event) {
  const filterButton = event.target.closest("[data-filter]");
  if (filterButton) {
    location.hash = `#games/${filterButton.dataset.filter}`;
    return;
  }

  const launchButton = event.target.closest("[data-launch]");
  if (launchButton) {
    launchGame(launchButton.dataset.launch);
  }
}

function launchGame(gameId) {
  location.hash = `#play/${gameId}`;
}

function getGenres() {
  return genreMeta.map((genre) => ({
    ...genre,
    name: copy.genres[genre.id].name,
    description: copy.genres[genre.id].description,
  }));
}

function getGenre(id) {
  return getGenres().find((genre) => genre.id === id) || getGenres()[0];
}

function getGames() {
  const visiblePuzzleExamples = new Set(["Tetris", "Minesweeper", "Sudoku"]);
  const catalog = [
    ...gameMeta,
    ...puzzleExamples
      .filter((game) => visiblePuzzleExamples.has(game.title))
      .map((game) => ({
        id: puzzleExampleId(game.title),
        genre: "puzzle",
        title: game.title,
        icon: game.icon,
        accent: "#2bd1c4",
        description: game.description,
        makeSrcdoc: () => makePuzzleExampleGame(game),
      })),
  ];

  return catalog.map((game) => ({
    ...game,
    description: copy.gameDescriptions[game.id] || translations.en.gameDescriptions[game.id] || game.description,
    srcdoc: game.makeSrcdoc ? game.makeSrcdoc() : undefined,
  }));
}

function puzzleExampleId(title) {
  return `puzzle-${title.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-|-$/g, "")}`;
}

function escapeAttribute(value) {
  return value
    .replaceAll("&", "&amp;")
    .replaceAll('"', "&quot;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;");
}

function jsString(value) {
  return JSON.stringify(value);
}

function localized(key) {
  return copy[key] || translations.en[key] || key;
}

