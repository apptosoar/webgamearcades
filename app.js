function render() {
  const [route, id] = location.hash.replace("#", "").split("/");

  if (!route || route === "home") {
    renderHome();
    return;
  }

  if (route === "genres") {
    renderGenres();
    return;
  }

  if (route === "games") {
    renderGames(id || "all");
    return;
  }

  if (route === "play") {
    renderPlayer(id);
    return;
  }

  renderHome();
}

